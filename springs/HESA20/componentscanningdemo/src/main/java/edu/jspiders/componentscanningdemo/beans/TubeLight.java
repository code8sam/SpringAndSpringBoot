package edu.jspiders.componentscanningdemo.beans;

import org.springframework.stereotype.Component;

@Component
public class TubeLight implements Light
{
	public TubeLight()
	{
		System.out.println("TubeLight Object is created!!!");
	}

	@Override
	public void on()
	{
		System.out.println("Tube light is ON!!!");
	}

	@Override
	public void off() 
	{
		System.out.println("Tube light is OFF!!!");
	}
	
}
